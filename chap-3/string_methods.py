# string methods
# len(), capitalize(), upper(), lower(), find(), replace(), startswith(), endswith() etc

name = "bidhAn"

print("Length of string is: ", len(name)) # 6
print(name.capitalize()) # Bidhan
print(name.upper()) # BIDHAN
print(name.lower()) # bidhan
print(name.find("d")) # return index 2
print(name.replace("A", "a")) # bidhan
print(name.startswith("bid")) # TRUE
print(name.endswith("han")) # False




