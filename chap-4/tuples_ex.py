# tuple example and methods -> It is immutable that means it cannot be changed 

# count(value), index(value), len(), min(), max(), etc

tuple_example_mix = (1,2,3,"bidhan", True, 3)
tuple_example_numeric = (1,2,3,5)

print(tuple_example_mix.count(3)) # 2 ota cha
print(tuple_example_mix.index("bidhan")) # index 3 return garcha

print(min(tuple_example_numeric)) # 1 value output dincha
print(max(tuple_example_numeric)) # 5 value output dincha

